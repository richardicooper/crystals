      INCLUDE 'STORE.INC'
      INCLUDE 'XUNITS.INC'
      INCLUDE 'XSSVAL.INC'
      INCLUDE 'XCONST.INC'
      INCLUDE 'XLST02.INC'
C
      COMMON /XLST05/ICOM05
      INCLUDE 'XIOBUF.INC'
