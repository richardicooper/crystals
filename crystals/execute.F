      M5=LOCB
      M5A=LOCA
      ISTORE(M5)=ISTORE(M5A)
      J=M5A+1
      K=M5A+3
      M5=M5+1
C--CHECK THAT THE SYMMETRY OPERATORS ARE VALID
      M=IABS(ISTORE(LOCD+7))-1
      IF(M)1550,1000,1000
