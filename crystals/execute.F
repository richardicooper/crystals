      J=NFL
      NFL=NFL+3
      IF(NFL-JI)2750,1900,1900
C--MOVE THE PARAMETER INFORMATION
2750  CONTINUE
      CALL XMOVE(STORE(I+1),STORE(J+1),2)
      ISTORE(J+1)=ISTORE(J+1)+JH
      I=ISTORE(I)
      GOTO 2600
      END
