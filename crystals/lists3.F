CODE FOR XPCHAT
      SUBROUTINE XPCHAT(NUMB,IADDD,LENGRP,NGRP)
C--GET ATOM REFERENCES IN LEXICAL LISTS IN FORMAT 'TYPE(SERIAL)'.
C      (SEE ALSO XPRTLC, XPCHLX)
C
C  NUMB    THE CARD NUMBER.
C  IADDD   THE DISC ADDRESS OF THE LOGICAL CARD.
C  LENGRP  THE LENGTH OF EACH REAL CARD.
C  NGRP    THE NUMBER OF REAL CARDS IN THIS LOGICAL CARD.
C
C--
      CHARACTER *88 CLINE
C
      DIMENSION CARD(20)
C
      INCLUDE 'XUNITS.INC'
      INCLUDE 'XSSVAL.INC'
C
C--MARK THIS AS THE FIRST REAL CARD
      J=0
C--FIND THE ADDRESS AND LENGTH POINTERS
      N=NGRP
      M=LENGRP
      L=IADDD
C--FIND THE LENGTH TO PRINT
      K=MIN0(M,20)
C--CHECK IF THERE ARE MORE CARDS TO PRINT
1000  CONTINUE
      IF(N)1050,1050,1100
C--NO MORE CARDS  -  RETURN
1050  CONTINUE
      RETURN
C--READ THIS IMAGE DOWN
1100  CONTINUE
      CALL XDOWNF(L,CARD(1),K)
C--UPDATE THE POINTERS
      L=L+KINCRF(M)
      N=N-1
C--CHECK IF THIS IS THE FIRST CARD
      WRITE(CLINE,1200)  CARD
      CALL XCTRIM (CLINE, NCHAR)
      DO 2000 I=1, NCHAR-1
       IX=INDEX(CLINE(I:I), '(')
       IF (IX .EQ. 0) GOTO 2000
        DO 2050 J=I+1, NCHAR 
         IF( INDEX('0123456789', CLINE(J:J)) .NE. 0) GOTO 2050
         IF( INDEX(')', CLINE(J:J)) .NE. 0) GOTO 2000
         CLINE(J:J) = ' '
2050    CONTINUE
2000  CONTINUE
      CALL XCREMS(CLINE, CLINE, NCHAR)
      IX = INDEX(CLINE, '( )')
      IF (IX .NE. 0) CLINE(IX:IX+2) = '  '
      IX = INDEX(CLINE, ' )')
      IF (IX .NE. 0) CLINE(IX:IX+1) = ') '
      CALL XCREMS(CLINE, CLINE, NCHAR)
      CALL XCTRIM (CLINE, NCHAR)
      KK = MIN0 ( 80, NCHAR)
      WRITE(NCPU,'(A)')  CLINE(1:KK)
      IF (NCHAR .GE. 81) THEN
       KK = KK + 1
       WRITE(NCPU,'(A)')  CLINE(KK:NCHAR)
      ENDIF
1200  FORMAT(20A4)
      J=1 + J
      GOTO 1000
      END
