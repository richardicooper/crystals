      SUBROUTINE XCOLL
C----- COLLECT
C--CONDENSE A LIST 5 OR LIST 10 AND ASSEMBLE MOLECULAR FRAGMENTS
C
C  LN1    INPUT LIST TYPE
C  LN2    OUTPUT LIST TYPE
