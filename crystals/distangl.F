      ICURR = ICURR - 1
      IF (ICHNG .EQ. 0) THEN
            K = LFNVC + MDFNVC*(NFNVC-1)
            DO 3370 I = LFNVC, K, MDFNVC
            IF (ISTORE(I) .EQ. 1) THEN
                  ISTORE(I) = ICURR
                  ICHNG = ICHNG + 1
                  GOTO 3380
