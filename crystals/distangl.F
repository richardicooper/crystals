C----- ERROR
            GOTO 9920
      ELSE IF (NDIST .EQ. 0) THEN
        IF (ITYPE .LT. 0) THEN
            IF (ISTORE(MFNVCA) .EQ. 1) THEN
C----- AN ISOLATED ATOM
                  WRITE(NCAWU,1451) ISTORE(M5A), STORE(M5A+1)
