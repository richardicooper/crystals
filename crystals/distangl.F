2250  CONTINUE
      IF(ABS(XOCC(K)-XOCC(M5A))-ZERO)2300,2300,2400
C--COMPARE THE CURRENT MINIMUM DISTANCE
2300  CONTINUE
      IF(STORE(I+10)-E)2350,2400,2400
