1700  CONTINUE
      IF(IRIC-JRIC)1750,1950,1950
C--CHECK IF THIS IS THE SAME ATOM
1750  CONTINUE
      IF(ISTORE(IRIC)-ISTORE(K))1950,1800,1950
C--CHECK THE DISTANCE
1800  CONTINUE
      IF(STORE(IRIC+10)-E)1850,1900,1900
1850  CONTINUE
