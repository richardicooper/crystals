C--CALLL THE TIMING FUNCTION
      CALL XTIME1(2)
C -- READ THE REST OF THE INSTRUCTIONS AND CHECK FOR ERRORS
      IF (   KRDDPV ( PROCS(1) , IDIMN )    .LT.   0   ) GO TO 9910
C
&PPCCS***
&PPC      CALL SETSTA( 'Collect' )
