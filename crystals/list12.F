      GOTO 1500
C--RELATIVE VALUES  -  CHECK FOR REFINEMENT
1400  CONTINUE
      IF(ISTORE(N))1450,1450,1500
C--NO REFINEMENT
1450  CONTINUE
      ISTORE(N)=-1
1500  CONTINUE
C--MOVE ONTO THE NEXT PART FOR THIS GROUP
      J=ISTORE(J)
      GOTO 1150
C--MOVE ONTO THE NEXT GROUP
1550  CONTINUE
      I=ISTORE(I)
      GOTO 1050
C--SET THE BLOCKING ADDRESSES
1600  CONTINUE
      CALL XMOVE(ICOM22(5),ICOM12(9),4)
      RETURN
C
9900  CONTINUE
C -- ERROR EXIT
      RETURN
      END
