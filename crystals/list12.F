C          STACK AT 'ADD'.
C  IADD    THE ADDRESS OF THE PARTIAL DERIVATIVE STACK  -  SET ON RETURN
C  IADDT   THE ADDRESS TO WHICH ALL NON-PARAMETERS ARE LINKED.
C          (THIS VARIABLE IS THE SAME AS 'L22PD').
C
