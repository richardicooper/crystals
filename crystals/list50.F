      FUNCTION KIVAL ( INSTR, CINSTR, LINSTR)
C
C----- GIVEN AN INSTRUCTION NAME OR NUMBER, CHECKS ITS
C      VALIDITY AND RETURNS DIRECTIVE INFO
      CHARACTER *(*) CINSTR
      DIMENSION ITEXT(15)
      INCLUDE 'ISTORE.INC'
C
      INCLUDE 'STORE.INC'
      INCLUDE 'XLST50.INC'
      INCLUDE 'XUNITS.INC'
