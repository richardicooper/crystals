CODE FOR XSETCC
      SUBROUTINE XSETCC
C--SET THE COMPILER CONSTANTS BEFORE EACH PARSE PASS.
C
C--
      INCLUDE 'ISTORE.INC'
C
      INCLUDE 'STORE.INC'
      INCLUDE 'XCNTRL.INC'
      INCLUDE 'XCONST.INC'
C
      INCLUDE 'QSTORE.INC'
C
C--SET UP THE BASE ADDRESS FOR THE CHAINS FROM THE BOTTOM OF CORE
      LCG=NFL
C--SET UP THE GENERAL OR CODE HEADER BLOCK
      ISTORE(LCG)=NOWT
      ISTORE(LCG+1)=NOWT
      ISTORE(LCG+2)=NOWT
      STORE(LCG+3)=1.0
      STORE(LCG+4)=1.0
      MDCG=LCG
      MCG=LCG+5
      NCG=0
C--SET THE FLAGS TO INDICATE THAT NO ATOMS HAVE BEEN INPUT THUS FAR
      NCA=0
      LCA=NOWT
      MCA=LCA
      MDCA=NOWT
C--SET UP THE WORK STACK AND CONSTANT HEADER
      LCS=MCG
      ISTORE(LCS)=NOWT
      ISTORE(LCS+1)=6
      ISTORE(LCS+2)=LFL-LC
      ISTORE(LCS+4)=NOWT
      ISTORE(LCS+5)=0
C--SET UP THE WORK STACK CONTROL VARIABLES
      MCS=LCS+4
      MCG=MCG+6
C--SET UP THE NEXT FREE LOCATION IN THE WORK STACK IN 'MDCS'
      MDCS=ISTORE(LCS+2)+1
      NCS=0
      RETURN
      END
