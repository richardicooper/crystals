1400  CONTINUE
      MDCS=MDCS-MDUV
      MUV=-(LFL+1)
      I=-MUV+2
      J=MF+2
C--MOVE THE NAME
      DO 1450 K=1,LK
      ISTORE(I)=ISTORE(J)
      I=I+1
      J=J+1
1450  CONTINUE
      GOTO 1550
C--THIS NAME IS ALREADY PRESENT
1500  CONTINUE
      MUV=(MUV-1)*MDUV+LUV
